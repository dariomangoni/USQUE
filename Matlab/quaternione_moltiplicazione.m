function quaternione_prodotto = quaternione_moltiplicazione (quat1, quat2)
quaternione_prodotto=[quat2(1), -quat2(2), -quat2(3), -quat2(4);...
					  quat2(2),  quat2(1), -quat2(4),  quat2(3);...
					  quat2(3),  quat2(4),  quat2(1), -quat2(2);...
					  quat2(4), -quat2(3),  quat2(2),  quat2(1)]*quat1;
end
